    private void init() {
        LoadList();
        addDocuListener();
        addTableSelectListener();
    }

    private void addTableSelectListener() {
        jtApps.getSelectionModel().addListSelectionListener(
                new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                int viewRow = jtApps.getSelectedRow();
                if (viewRow < 0) {
                    return;
                } else {
                    int modelRow = jtApps.convertRowIndexToModel(viewRow);
                    id = (Integer) jtApps.getModel().getValueAt(modelRow, 0);
                }
            }
        }
        );
    }

    private void addDocuListener() {
        jtfSearchApp.getDocument().addDocumentListener(
                new DocumentListener() {
            public void changedUpdate(DocumentEvent e) {
                newFilter();
            }

            public void insertUpdate(DocumentEvent e) {
                newFilter();
            }

            public void removeUpdate(DocumentEvent e) {
                newFilter();
            }

            private void newFilter() {
                RowFilter<DefaultTableModel, Object> rf = null;
                try {
                    rf = RowFilter.regexFilter(jtfSearchApp.getText(), 0);
                } catch (java.util.regex.PatternSyntaxException e) {
                    return;
                }
                appSorter.setRowFilter(rf);
            }
        });
    }

    DefaultTableModel dtm = new DefaultTableModel();

    private void LoadList() {
        dtm.setColumnIdentifiers(Appointment.appointmentColumns());

        List<Appointment> appointments = PATIENT_VIEW.getAppointments();

        for (int i = 0; i < appointments.size(); i++) {

            Object[] row = new Object[Appointment.appointmentColumns().length];
            row[0] = appointments.get(i).getiDAppoint();
            row[1] = appointments.get(i).getTitle();
            row[2] = appointments.get(i).getPreferredDate().toLocalDate();
            row[3] = appointments.get(i).getDoctorID();
            row[4] = appointments.get(i).getOutpatientID();

            dtm.addRow(row);
        }
        
        appSorter = new TableRowSorter<DefaultTableModel>(dtm);

        jtApps.setModel(dtm);
        jtApps.getTableHeader().setFont(new Font("Calibri", Font.BOLD, 14));
        jtApps.setRowHeight(20);
        jtApps.setRowSorter(appSorter);
        jtApps.setDefaultEditor(Object.class, null);

        dtm.addTableModelListener((TableModelEvent tme) -> {
            DefaultTableModel tm = (DefaultTableModel) tme.getSource();
        });
    }

    public void reset() {
        resetForm(jpList);
        jtfSearchApp.setText("");
        ListSelectionModel select = jtApps.getSelectionModel();
        select.setSelectionInterval(0, 0);
        select.setSelectionMode(0);
    }

