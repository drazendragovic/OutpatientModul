    public void loadPatientById(int iDPat) {

        jtpPatDet.setSelectedIndex(0);
        btnAppDeta.setVisible(true);
        Outpatient patient = PATIENT_VIEW.getPatientById(iDPat);

        resetForm(jpBasDet);

        if (patient == null) {
            return;
        } else {
            patID = patient.getiDOutpat();
            perID = patient.getPersonID();

            jtfPatID.setText(Integer.toString(patient.getiDOutpat()));
            jtfName.setText(patient.getFirstName());
            jtfMidName.setText(patient.getMiddleName());
            jtfSurname.setText(patient.getSurname());
            jtfName.setText(patient.getFirstName());
            jtfBirth.setText(patient.getDateOFBirth().toLocalDate().toString());
            Boolean gender = patient.getSex();
            if (gender.equals(true)) {
                rbMale.setSelected(true);
            } else {
                rbFemale.setSelected(true);
            }
            jtfMariState.setText(patient.getMaritalStatus());
            jtfOccupa.setText(patient.getOccupation());
            jtfDepenNo.setText(Integer.toString(patient.getNoDependents()));
            jtfGai.setText(Double.toString(patient.getGaIncome()));
            jtfOpid.setText(Integer.toString(patient.getoPNumber()));
            jtfHeight.setText(Double.toString(patient.getoPHeight()));
            jtfWeight.setText(Double.toString(patient.getOpWeight()));
            jtfBloodTyp.setText(patient.getOpBloodType());

        }
    }

    private void loadContDetails() {
        Contact patCont = PERSON_VIEW.getPersonContact(perID);
        PersonAddress patAddr = PERSON_VIEW.getPersonAddress(perID);

        if (patCont == null || patAddr == null) {
            InitPreComBox();
            InitPerComBox();
        } else {
            resetForm(jpContDet);

            if (patAddr.getAddrTypeID() == 1) {

                InitPreComBox();
                jrbPresent.setSelected(true);

                jtfPreAdd.setText(patAddr.toString());
                jcbPreCity.setSelectedIndex(patAddr.getCityID() - 1);
                jcbPreArea.setSelectedIndex(patAddr.getAreaID() - 1);
                jcbPreState.setSelectedIndex(getCountryId(patAddr.getCityID()) - 1);

                jrbPermanent.setSelected(false);
                jtfPerAdd.setEditable(false);
                jcbPerCity.setEnabled(false);
                jcbPerArea.setEnabled(false);
                jcbPerState.setEnabled(false);

            } else {

                InitPerComBox();
                jrbPermanent.setSelected(true);

                jtfPerAdd.setText(patAddr.toString());
                jcbPerCity.setSelectedIndex(patAddr.getCityID() - 1);
                jcbPerArea.setSelectedIndex(patAddr.getAreaID() - 1);
                jcbPerState.setSelectedIndex(getCountryId(patAddr.getCityID()) - 1);

                jrbPresent.setSelected(false);
                jtfPreAdd.setEditable(false);
                jcbPreCity.setEnabled(false);
                jcbPreArea.setEnabled(false);
                jcbPreState.setEnabled(false);

            }
        }
        jtfTelHom.setText(patCont.getHomeTel());
        jtfTelWor.setText(patCont.getWorkTel());
        jtfMob.setText(patCont.getMobile());
        jtfMail.setText(patCont.getEmail());
    }

