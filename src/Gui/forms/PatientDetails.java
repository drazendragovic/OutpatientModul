    DefaultTableModel dtm = new DefaultTableModel();

    public void loadList(int idPat) {

        dtm.setRowCount(0);

        dtm.setColumnIdentifiers(Appointment.appointmentPatColumns());

        List<Appointment> appointments = PATIENT_VIEW.getAppointmentsForPatient(idPat);

        for (int i = 0; i < appointments.size(); i++) {

            Object[] row = new Object[Appointment.appointmentPatColumns().length];
            row[0] = appointments.get(i).getiDAppoint();
            row[1] = appointments.get(i).getTitle();
            row[2] = appointments.get(i).getPreferredDate().toLocalDate();
            row[3] = appointments.get(i).getDoctorID();

            dtm.addRow(row);
        }

        jtblPatApp.setModel(dtm);
        jtblPatApp.getTableHeader().setFont(new Font("Calibri", Font.BOLD, 14));
        jtblPatApp.setRowHeight(20);

        ListSelectionModel select = jtblPatApp.getSelectionModel();
        select.setSelectionInterval(0, 0);
        select.setSelectionMode(0);
        jtblPatApp.setDefaultEditor(Object.class, null);

        dtm.addTableModelListener((TableModelEvent tme) -> {
            DefaultTableModel tm = (DefaultTableModel) tme.getSource();
        });
    }

